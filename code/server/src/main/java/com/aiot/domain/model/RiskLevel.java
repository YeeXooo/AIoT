package com.aiot.domain.model;

/**
 * 风险等级（VO-01）
 * 统一的三级风险严重度标签，贯穿全系统判定与干预逻辑
 * 仅表达风险严重程度，不承载"是否解除"状态（解除由独立领域事件承载）
 *
 * L1_HINT 为预留等级，本期无业务触发路径，保留用于未来扩展
 * 所有分支匹配必须覆盖该取值，满足编译期穷尽检查
 */
public enum RiskLevel {
    /** 提示级（预留，本期无触发路径） */
    L1_HINT,
    /** 警告级（疲劳/分心/路怒等中度风险） */
    L2_WARNING,
    /** 紧急级（重度疲劳/活体遗留/碰撞失能等高危风险） */
    L3_CRITICAL
}